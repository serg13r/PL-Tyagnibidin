# Вариант 7 Дана строка. Преобразовать ее, заменив звездочками все буквы "п",
# встречающиеся среди первых n/2 символов. Здесь n - длина строки.
s=input ('Вариант 7: Введите строку \n')
counter = 0
index = len(s)//2
while 'п' in s[0:index]:
    s = s.replace('п','*',1)
    counter += 1
print('Итоговая строка:', s,
      '\nКол. замен:', counter)


