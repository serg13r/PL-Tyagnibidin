# Вариант 1 Дана строка, содержащая русскоязычный текст. Найти количество слов,
# начинающихся с буквы "е".
s=input ('Вариант 1: Введите строку \n')
counter = 0
for i in s.split():
    if i[0] == 'е' or i[0] == 'Е':
        counter += 1
print('Кол. слов начинающихся с \'е\'', counter)


# Вариант 2 В строке заменить все двоеточия (:) знаком процента (%). Подсчитать количество
# замен.
s=input ('Вариант 2: Введите строку \n')
counter = 0
while ':' in s:
    s = s.replace(':','%',1)
    counter += 1
print('Итоговая строка:', s,
      '\nКол. замен:', counter)


# Вариант 3 В строке удалить символ точку (.) и подсчитать количество удаленных символов
s=input ('Вариант 3: Введите строку \n')
counter = 0
while '.' in s:
    s = s.replace('.','',1)
    counter += 1
print('Итоговая строка:', s,
      '\nКол. удалений:', counter)


