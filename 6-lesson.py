# Вариант 11 Дана строка. Подсчитать самую длинную последовательность подряд идущих
# букв «н». Преобразовать ее, заменив точками все восклицательные знаки.
s=input ('Вариант 11: Введите строку \n')
max_n_count = 0
current_n_count = 0
for char in s:
    if char == 'н':
        current_n_count += 1
    else:
        max_n_count = max(max_n_count, current_n_count)
        current_n_count = 0

max_n_count = max(max_n_count, current_n_count)
print('Самая длинная последовательность подряд идущих букв «н»:',
      max_n_count,
      '\nПреобразованная строка:',
      s.replace('!', '.'))


